async def search_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = " ".join(context.args).strip() if context.args else ""
    if not query:
        await update.message.reply_text(
            "أرسل كلمة البحث بعد الأمر، مثال:\n`/search الحيض`",
            parse_mode="Markdown"
        )
        return

    results = []
    for key, data in CONTENT.items():
        if query in data["title"] or query in data["text"]:
            results.append((data["title"], key))

    if not results:
        await update.message.reply_text(f"لم أجد نتائج لـ «{query}»، جرب كلمة أخرى.")
        return

    keyboard = [[InlineKeyboardButton(title, callback_data=f"content_{key}")] for title, key in results[:10]]
    keyboard.append([InlineKeyboardButton("🏠 القائمة الرئيسية", callback_data="main_menu")])
    await update.message.reply_text(
        f"نتائج البحث عن «{query}» ({len(results)} نتيجة):",
        reply_markup=InlineKeyboardMarkup(keyboard)
    )


async def button_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    data = query.data

    if data == "main_menu":
        await query.edit_message_text(
            "🏠 *القائمة الرئيسية*\n\nاختر القسم الذي تريده:",
            parse_mode="Markdown",
            reply_markup=main_menu_keyboard()
        )

    elif data in CATEGORIES:
        cat = CATEGORIES[data]
        await query.edit_message_text(
            f"*{cat['title']}*\n\nاختر الموضوع:",
            parse_mode="Markdown",
            reply_markup=category_keyboard(data)
        )

    elif data.startswith("content_"):
        content_key = data[len("content_"):]
        if content_key in CONTENT:
            item = CONTENT[content_key]
            cat_key = CONTENT_TO_CAT.get(content_key, "main_menu")
            await query.edit_message_text(
                item["text"],
                parse_mode="Markdown",
                reply_markup=back_keyboard(cat_key)
            )


async def message_handler(update: Update, context: ContextTypes.DEFAULT_TYPE):
    text = update.message.text or ""
    # بحث تلقائي بالنص
    results = []
    for key, data in CONTENT.items():
        if text in data["title"] or text in data["text"]:
            results.append((data["title"], key))

    if results:
        keyboard = [[InlineKeyboardButton(t, callback_data=f"content_{k}")] for t, k in results[:5]]
        keyboard.append([InlineKeyboardButton("🏠 القائمة الرئيسية", callback_data="main_menu")])
        await update.message.reply_text(
            f"وجدت نتائج لـ «{text}»:",
            reply_markup=InlineKeyboardMarkup(keyboard)
        )
    else:
        await update.message.reply_text(
            "لم أفهم رسالتك 🌿\n\n"
            "يمكنك:\n"
            "• الضغط على /start للقائمة الرئيسية\n"
            "• كتابة /search ثم الكلمة للبحث\n"
            "• أو ببساطة اكتب كلمة تريد البحث عنها",
            reply_markup=InlineKeyboardMarkup([
                [InlineKeyboardButton("🏠 القائمة الرئيسية", callback_data="main_menu")]
            ])
        )


# ─── التشغيل ──────────────────────────────────────────────────────────────────────

def main():
    token = os.environ.get("TELEGRAM_BOT_TOKEN")
    if not token:
        raise ValueError("TELEGRAM_BOT_TOKEN غير موجود في متغيرات البيئة!")

    app = Application.builder().token(token).build()

    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("help", help_command))
    app.add_handler(CommandHandler("search", search_command))
    app.add_handler(CallbackQueryHandler(button_handler))
    app.add_handler(MessageHandler(filters.TEXT & ~filters.COMMAND, message_handler))

    logger.info("بوت العلم الشرعي يعمل...")
    app.run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()
